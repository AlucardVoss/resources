function StartCheckingIfAtEnd()
    Citizen.CreateThread(function()
        while AtEnd do
            Citizen.Wait(5)
            if #(vector3(-172.45953369141, -33.659687042236, 52.218238830566) - GetEntityCoords(PlayerPedId())) <= 20 then
                DrawText3Ds(vector3(-172.45953369141, -33.659687042236, 52.218238830566), '[E] Return Vehicle')
                if #(vector3(-172.45953369141, -33.659687042236, 52.218238830566) - GetEntityCoords(PlayerPedId())) <= 5 and IsControlJustReleased(0, 38) then
                    if IsPedInAnyVehicle(PlayerPedId()) then
                        local vehicle = GetVehiclePedIsIn(PlayerPedId())
                        if vehicle == Infinite.Vehicle then
                            TriggerServerEvent('infinite-warehouse:returnveh', veh)
                            DoScreenFadeOut(500)
                            Citizen.Wait(1000)
                            DeleteEntity(vehicle)
                            DoingMission = false
                            AtEnd = false
                            return
                        else
                            TriggerEvent('DoLongHudText', 'Please come back in the contracted vehicle', 2)
                        end
                    else
                        TriggerEvent('DoLongHudText', 'You need to be in a vehicle to return the vehicle', 2)
                    end
                end
            else
                Citizen.Wait(1000)
            end
        end                
    end)
end

function DrawText3Ds(coords, text)
    local onScreen,_x,_y=World3dToScreen2d(coords.x, coords.y, coords.z)
    local px,py,pz=table.unpack(GetGameplayCamCoords())
